def _build_rows() -> list[ICD10Diagnosis]:
    combined = list(_BASE_ROWS)
    insert_at = 0
    for idx, (code, _title) in enumerate(combined):
        if code == "F20":
            insert_at = idx
            break
    combined[insert_at:insert_at] = _substance_rows()

    seen: set[str] = set()
    diagnoses: list[ICD10Diagnosis] = []
    for code, title in combined:
        if code in seen:
            continue
        seen.add(code)
        diagnoses.append(ICD10Diagnosis(code=code, title=title))
    return sorted(diagnoses, key=lambda d: _code_sort_key(d.code))


def _code_sort_key(code: str) -> tuple[int, ...]:
    digits = re.findall(r"\d+", code)
    return tuple(int(d) for d in digits)


