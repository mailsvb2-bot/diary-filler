    rf"^\s*(?<![A-Za-zА-ЯЁа-яё]){EXAMINEE_FORMS_PATTERN}(?![A-Za-zА-ЯЁа-яё])"
    r"\s*(?:[:,.;!\-–—]+)?\s*",
    re.IGNORECASE,
